#pragma once

//==========================================================
// Windows ヘッダー最小化設定
//==========================================================
#ifndef WIN32_LEAN_AND_MEAN
#define WIN32_LEAN_AND_MEAN
#endif

#include <windows.h>

namespace Engine {

//==========================================================
// 前方宣言
//==========================================================
class Renderer;
class InputManager;
class CollisionManager;

//==========================================================
// Systemクラス - エンジン全体を統括するシングルトン
//==========================================================
/**
 * \class System
 * \brief エンジンの中核となるシステムクラス
 * 
 * レンダラー、入力、衝突判定などの全サブシステムの
 * 初期化・更新・終了処理を一元管理する。
 * シングルトンパターンで実装されており、GetInstance()で取得する。
 */
class System {
private:
    // --- メンバ変数 ---
    bool m_initialized = false;     // 初期化完了フラグ
    float m_deltaTime = 0.0f;       // 前フレームからの経過時間（秒）
    double m_lastTime = 0.0;        // 前フレームの時刻（タイマー値）

    // --- プライベートコンストラクタ（シングルトン） ---
    System() = default;

public:
    //==========================================================
    // シングルトンアクセス
    //==========================================================
    static System& GetInstance();

    //==========================================================
    // ライフサイクル管理
    //==========================================================
    /**
     * \brief システム全体の初期化
     * \param hInstance アプリケーションインスタンスハンドル
     * \param hWnd ウィンドウハンドル
     * \param windowed ウィンドウモードで起動するか
     * \return 初期化成功時true、失敗時false
     */
    bool Initialize(HINSTANCE hInstance, HWND hWnd, bool windowed);
    
    /**
     * \brief システム全体の終了処理
     */
    void Finalize();
    
    /**
     * \brief フレーム更新処理（毎フレーム呼び出し）
     */
    void Update();

    //==========================================================
    // サブシステムアクセサ
    //==========================================================
    Renderer& GetRenderer();            // 描画システム取得
    InputManager& GetInput();           // 入力システム取得
    CollisionManager& GetCollision();   // 衝突判定システム取得

    //==========================================================
    // ユーティリティ
    //==========================================================
    float GetDeltaTime() const { return m_deltaTime; }      // デルタタイム取得
    bool IsInitialized() const { return m_initialized; }    // 初期化状態確認

    // --- コピー禁止（シングルトン） ---
    System(const System&) = delete;
    System& operator=(const System&) = delete;
};

//==========================================================
// グローバル便利関数
//==========================================================
System& GetSystem();    // Systemインスタンスへのショートカット
float GetDeltaTime();   // デルタタイム取得のショートカット

} // namespace Engine
