/*********************************************************************
 * \file   keyboard.h
 * \brief  キーボード入力モジュール
 *
 * \author Ryoto Kikuchi
 * \date   2026/3/10
 *
 * 【概要】
 * Windowsメッセージベースのキーボード入力管理
 * DirectXTKの設計を参考にモダンC++でリファクタリング
 *
 * 【特徴】
 * - Press/Trigger/Release の3種判定を標準装備
 * - 左右のShift/Ctrl/Altを区別
 * - ビットフィールドによる効率的な状態管理（256bit = 32byte）
 * - 従来のC言語APIとの互換性を維持
 *
 * 【使用方法】
 * 1. Keyboard::Instance().Initialize() で初期化
 * 2. WndProcから Keyboard::Instance().ProcessMessage() を呼ぶ
 * 3. ゲームループで Keyboard::Instance().Update() を呼ぶ
 * 4. IsPressed/IsTrigger/IsRelease で状態を取得
 *
 *********************************************************************/
#pragma once

#ifndef WIN32_LEAN_AND_MEAN
#define WIN32_LEAN_AND_MEAN
#endif
#include <windows.h>
#include <cstdint>

 //==============================================================================
 // キーコード定義
 //------------------------------------------------------------------------------
 // Windowsの仮想キーコード（VK_*）と同じ値
 // enum classで型安全性を確保
 //==============================================================================
enum class Key : uint8_t {
    None = 0x00,

    //==========================================================================
    // 制御キー
    //==========================================================================
    Backspace = 0x08,   // VK_BACK
    Tab = 0x09,   // VK_TAB
    Enter = 0x0D,   // VK_RETURN
    Pause = 0x13,   // VK_PAUSE
    CapsLock = 0x14,   // VK_CAPITAL
    Escape = 0x1B,   // VK_ESCAPE
    Space = 0x20,   // VK_SPACE

    //==========================================================================
    // ナビゲーションキー
    //==========================================================================
    PageUp = 0x21,   // VK_PRIOR
    PageDown = 0x22,   // VK_NEXT
    End = 0x23,   // VK_END
    Home = 0x24,   // VK_HOME
    Left = 0x25,   // VK_LEFT
    Up = 0x26,   // VK_UP
    Right = 0x27,   // VK_RIGHT
    Down = 0x28,   // VK_DOWN

    //==========================================================================
    // 編集キー
    //==========================================================================
    PrintScreen = 0x2C,   // VK_SNAPSHOT
    Insert = 0x2D,   // VK_INSERT
    Delete = 0x2E,   // VK_DELETE

    //==========================================================================
    // 数字キー（メインキーボード）
    //==========================================================================
    D0 = 0x30, D1 = 0x31, D2 = 0x32, D3 = 0x33, D4 = 0x34,
    D5 = 0x35, D6 = 0x36, D7 = 0x37, D8 = 0x38, D9 = 0x39,

    //==========================================================================
    // アルファベットキー
    //==========================================================================
    A = 0x41, B = 0x42, C = 0x43, D = 0x44, E = 0x45,
    F = 0x46, G = 0x47, H = 0x48, I = 0x49, J = 0x4A,
    K = 0x4B, L = 0x4C, M = 0x4D, N = 0x4E, O = 0x4F,
    P = 0x50, Q = 0x51, R = 0x52, S = 0x53, T = 0x54,
    U = 0x55, V = 0x56, W = 0x57, X = 0x58, Y = 0x59, Z = 0x5A,

    //==========================================================================
    // Windowsキー
    //==========================================================================
    LeftWindows = 0x5B,   // VK_LWIN
    RightWindows = 0x5C,   // VK_RWIN
    Apps = 0x5D,   // VK_APPS（メニューキー）

    //==========================================================================
    // テンキー
    //==========================================================================
    NumPad0 = 0x60, NumPad1 = 0x61, NumPad2 = 0x62, NumPad3 = 0x63,
    NumPad4 = 0x64, NumPad5 = 0x65, NumPad6 = 0x66, NumPad7 = 0x67,
    NumPad8 = 0x68, NumPad9 = 0x69,
    Multiply = 0x6A,   // VK_MULTIPLY  (*)
    Add = 0x6B,   // VK_ADD       (+)
    Separator = 0x6C,   // VK_SEPARATOR
    Subtract = 0x6D,   // VK_SUBTRACT  (-)
    Decimal = 0x6E,   // VK_DECIMAL   (.)
    Divide = 0x6F,   // VK_DIVIDE    (/)

    //==========================================================================
    // ファンクションキー
    //==========================================================================
    F1 = 0x70, F2 = 0x71, F3 = 0x72, F4 = 0x73,
    F5 = 0x74, F6 = 0x75, F7 = 0x76, F8 = 0x77,
    F9 = 0x78, F10 = 0x79, F11 = 0x7A, F12 = 0x7B,
    F13 = 0x7C, F14 = 0x7D, F15 = 0x7E, F16 = 0x7F,
    F17 = 0x80, F18 = 0x81, F19 = 0x82, F20 = 0x83,
    F21 = 0x84, F22 = 0x85, F23 = 0x86, F24 = 0x87,

    //==========================================================================
    // ロックキー
    //==========================================================================
    NumLock = 0x90,   // VK_NUMLOCK
    ScrollLock = 0x91,   // VK_SCROLL

    //==========================================================================
    // 修飾キー（左右区別）
    //==========================================================================
    LeftShift = 0xA0,   // VK_LSHIFT
    RightShift = 0xA1,   // VK_RSHIFT
    LeftControl = 0xA2,   // VK_LCONTROL
    RightControl = 0xA3,   // VK_RCONTROL
    LeftAlt = 0xA4,   // VK_LMENU
    RightAlt = 0xA5,   // VK_RMENU

    //==========================================================================
    // OEMキー（記号キー）
    // ※キーボードレイアウトによって異なる場合あり
    //==========================================================================
    Semicolon = 0xBA,   // VK_OEM_1     (; :)
    Plus = 0xBB,   // VK_OEM_PLUS  (= +)
    Comma = 0xBC,   // VK_OEM_COMMA (, <)
    Minus = 0xBD,   // VK_OEM_MINUS (- _)
    Period = 0xBE,   // VK_OEM_PERIOD (. >)
    Slash = 0xBF,   // VK_OEM_2     (/ ?)
    Tilde = 0xC0,   // VK_OEM_3     (` ~)
    OpenBracket = 0xDB,   // VK_OEM_4     ([ {)
    Backslash = 0xDC,   // VK_OEM_5     (\ |)
    CloseBracket = 0xDD,   // VK_OEM_6     (] })
    Quote = 0xDE,   // VK_OEM_7     (' ")
    Oem8 = 0xDF,   // VK_OEM_8
    OemBackslash = 0xE2,   // VK_OEM_102   (\ | 日本語キーボード)
};

//==============================================================================
// キーボードクラス
//------------------------------------------------------------------------------
// 【設計】
// - シングルトンパターンでグローバルアクセス
// - ビットフィールドで256キー分の状態を32バイトで管理
// - 現在フレームと前フレームの状態を保持してTrigger/Release判定
//==============================================================================
class Keyboard {
public:
    //==========================================================================
    // シングルトンアクセス
    //==========================================================================
    static Keyboard& Instance();

    //==========================================================================
    // ライフサイクル
    //==========================================================================

    /// 初期化（ゲーム起動時に1回）
    void Initialize();

    /// 更新（毎フレーム、入力処理の最初に呼ぶ）
    /// 前フレームの状態を保存する
    void Update();

    /// 状態リセット（ウィンドウ非アクティブ時などに呼ぶ）
    void Reset();

    //==========================================================================
    // Windowsメッセージ処理
    //==========================================================================

    /// WndProcから呼び出す
    /// @param message WM_KEYDOWN, WM_KEYUP, WM_SYSKEYDOWN, WM_SYSKEYUP, WM_ACTIVATEAPP
    /// @param wParam 仮想キーコード
    /// @param lParam キーフラグ
    void ProcessMessage(UINT message, WPARAM wParam, LPARAM lParam);

    //==========================================================================
    // 状態取得
    //==========================================================================

    /// キーが押されているか（押している間ずっとtrue）
    /// @param key 調べるキー
    /// @return 押されていればtrue
    bool IsPressed(Key key) const;

    /// キーが押された瞬間か（押したフレームだけtrue）
    /// @param key 調べるキー
    /// @return 押した瞬間ならtrue
    bool IsTrigger(Key key) const;

    /// キーが離された瞬間か（離したフレームだけtrue）
    /// @param key 調べるキー
    /// @return 離した瞬間ならtrue
    bool IsRelease(Key key) const;

    /// いずれかのキーが押されているか
    /// @return 何かキーが押されていればtrue
    bool IsAnyKeyPressed() const;

private:
    //==========================================================================
    // コンストラクタ（シングルトンのためprivate）
    //==========================================================================
    Keyboard() = default;
    ~Keyboard() = default;
    Keyboard(const Keyboard&) = delete;
    Keyboard& operator=(const Keyboard&) = delete;

    //==========================================================================
    // 内部ヘルパー
    //==========================================================================

    /// キーのビットを設定
    /// @param vk 仮想キーコード
    /// @param down trueなら押下、falseなら解放
    void SetKey(int vk, bool down);

    /// 指定キーのビットが立っているか
    /// @param state 状態配列
    /// @param key キーコード
    /// @return ビットが立っていればtrue
    bool GetBit(const uint32_t* state, int key) const;

private:
    //==========================================================================
    // 状態データ
    //==========================================================================
    // 256bit = 8 x 32bit = 32byte
    // 各キーは1bitで表現（0=離されている、1=押されている）
    static constexpr int STATE_SIZE = 8;

    uint32_t m_currentState[STATE_SIZE] = {};   ///< 現在フレームの状態
    uint32_t m_previousState[STATE_SIZE] = {};  ///< 前フレームの状態
};
